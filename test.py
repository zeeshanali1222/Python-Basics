# Python by default uses space as a separator
# We can change the separator using sep parameter in print function

print("Pakistan", "India", "China")
print("Pakistan", "India", "China", sep="")
print("Pakistan", "India", "China", sep=",")

# Print function by default ends with a new line

print("Pakistan1")
print("Pakistan2")
print("Pakistan3")

# We can change the end character using end parameter in print function

print("Pakistan1", end="")
print("Pakistan2", end="")
print("Pakistan3")
print(end="\n")

print("Pakistan1", end="\t")
print("Pakistan2", end="\t")
print("Pakistan3")
